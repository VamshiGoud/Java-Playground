class Allvars
{
	public static void main(String args[])
	{
		int x = 1;
		Boolean y = true;
		char z = 'x';
		float w = 1.2388564f;
		double i = 1.85545556816816818618;

		System.out.println(x + " is Integer type");
		System.out.println(y + " is Boolean type");
		System.out.println(z + " is char type");
		System.out.println(w + " is float type");
		System.out.println(i + " is double type");

	}
}
